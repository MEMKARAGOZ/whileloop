public class WhileLoop {
    public static void main(String[] args) {
        System.out.println("while loop 0<==>10");
        int i = 0;
        while (i <= 10) {
            System.out.println(i);
            i++;

        }
        System.out.println("while loop 0<==>9");
        int x = 0;
        while (x < 10) {
            System.out.println(x);
            x++;
        }
        System.out.println("while loop 0<==>10 çift sayılar");
        int y = 0;
        while (y <= 10) {
            System.out.println(y);
            y+=2;
        }
        System.out.println("while loop 0<==>10 tek sayılar");
        int z = 1;
        while (z <= 10) {
            System.out.println(z);
            z+=2;
        }

        System.out.println("While loops bitti");

    }
}